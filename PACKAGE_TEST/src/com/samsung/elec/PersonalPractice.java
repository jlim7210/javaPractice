package com.samsung.elec;

public class PersonalPractice {
	String name;
	int number;
	
	PersonalPractice(){}
	PersonalPractice(String name){
		this.name = name;
	}
	PersonalPractice(int num){
		this.number = num;
	}
	PersonalPractice(String name, int num){
		this.name = name;
		this.number = num;
	}
}
